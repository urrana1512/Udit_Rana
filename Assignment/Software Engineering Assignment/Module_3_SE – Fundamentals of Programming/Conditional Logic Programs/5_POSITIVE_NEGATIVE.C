#include <stdio.h>
void main()
{
    float n;

    // GET VALUE FROM USER
    printf("Enter Number : ");
    scanf("%d", &n);

    // Check if N is greater than 0 or not
    if (n > 0)
    {
        printf("Positive..!", n);
    }
    else
    {
        printf("Negative..!", n);
    }
}
