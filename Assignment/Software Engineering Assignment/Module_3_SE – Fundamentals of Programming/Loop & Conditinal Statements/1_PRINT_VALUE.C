// PROGRAM 1 : PRINT 972 to 897 USING FOR LOOP - (START) --------------------->
#include <stdio.h>
void main()
{
    int i;

    // loop starting from 972
    // condition : when i is >=897 then loop is terminated
    for (i = 972; i >= 897; i--)
    {
        printf("%d\n", i);
    }
}
// PROGRAM 1 : PRINT 972 to 897 USING FOR LOOP - (END) --------------------->